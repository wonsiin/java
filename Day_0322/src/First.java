// 첫 번째 프로그램 First = class
public class First {
	// 메인 메소드 : 프로그램의 시작위치
	public static void main(String[] args) {
		System.out.println("Hello, world!"); //statement  클래스 안에 메소드 안에 문장(모든 문장은 매소드 안에있어야함)
		System.out.println("안녕? 자바!!!");  //line : ln(줄바꿈) (문자열:String)
		System.out.println("프로그램을 작성했어요!!!");
	}
}
